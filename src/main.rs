fn main() {

  //Basic Operations with values - mathematics
  let a = 10;
  let b = 3;

  println!("Addition: {}", a + b);        
  println!("Subtraction: {}", a - b);     
  println!("Multiplication: {}", a * b); 
  println!("Division: {}", a / b);       
  println!("Remainder: {}", a % b); 
  
  //Basic Operations with values - comparing
  let x = 5;
  let y = 10;

  println!("x == y: {}", x == y); 
  println!("x != y: {}", x != y); 
  println!("x > y: {}", x > y);   
  println!("x < y: {}", x < y);   
  println!("x >= y: {}", x >= y); 
  println!("x <= y: {}", x <= y); 

  //create new values and operations on it
  let num1 = 20;
  let num2 = 30;
  println!("Addition: {}", num1 + num2);
  println!("Subtraction: {}", num1 - num2);
  println!("Multiplication: {}", num1 * num2);
  println!("Division: {}", num1 / num2);
  println!("Remainder: {}", num1 % num2);
  println!("Is num1 == num2? {}", num1 == num2);
  println!("Is num1 > num2? {}", num1 > num2);
  println!("Is num2 <= num1? {}", num2 <= num1);
}