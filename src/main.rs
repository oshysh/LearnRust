// Variables and types of data

fn main() {
  // Immutable variable
  let x = 5;
  println!("The value of x is: {}", x);

  // Mutable variable
  let mut y = 10;
  println!("The value of y is: {}", y);

  // Changing the value of y
  y = 15;
  println!("The new value of y is: {}", y);

  11111
}